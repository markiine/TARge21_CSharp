using System;
namespace KäsudMitmesFailis
{
    public class Abivahendid
    {
        public static int korruta(int a, int b)
        {
            return a * b;
        }

        public static int liida(int a, int b)
        {
            return a + b;
        }

        public static int astenda(int a, int b)
        {
            return (int) Math.Pow(a, b);
        }
    }
}
